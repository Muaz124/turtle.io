/**
 * Sets a handler for all methods
 *
 * @method all
 * @param  {String}   route RegExp pattern
 * @param  {Function} fn    Handler
 * @param  {String}   host  [Optional] Virtual host, default is `all`
 * @return {Object}         TurtleIO instance
 */
all ( route, fn, host ) {
	array.each( VERBS, ( i ) => {
		this.use( route, fn, host, i );
	} );

	return this;
}

